public class Main {
    public static void main(String[] args) {
        System.out.println("Hello World!");
    }
}

// System.out.printIn -> console.log, print랑 같은 애
// public -> 다른데서도 쓸 수 있게 공개로
// static -> 클래스명으로 접근 가능
// void -> 리턴값이 없음(실행만 하고 끝)
// main -> 메인 함수(진입점)
// String[] args -> 매개변수

// Main: 이건 클래스 이름 (자바는 클래스 중심 언어야)

// public static void main(...): 자바 프로그램 시작점

// System.out.println(...): 출력 함수 (자바의 console.log() / print() 같은 거)

